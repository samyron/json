    // IEEE 754 double precision constants
    static final int DOUBLE_MANTISSA_BITS = 52;
    static final int DOUBLE_EXPONENT_BITS = 11;
    static final int DOUBLE_EXPONENT_BIAS = 1023;

    // Helper: floor(log2(value)) using ryu_leading_zeros64
    static int floorLog2(long value) {
        return 63 - Long.numberOfLeadingZeros(value);
    }

    // Helper: log2(5^e) approximation
    static int log2pow5(int e) {
        return (e * 1217359) >> 19;
    }

    // Helper: ceil(log2(5^e))
    static int ceilLog2pow5(int e) {
        return log2pow5(e) + 1;
    }

    // Helper: convert uint64 bits to double
    static double int64Bits2Double(long bits) {
        return Double.longBitsToDouble(bits);
    }

    // Check if value is multiple of 2^p
    static boolean multipleOfPowerOf2(long value) {
        return (value & (value - 1)) == 0;
    }

    // Count how many times value is divisible by 5
    // Uses modular inverse to avoid expensive division
    static int pow5Factor(long value) {
        final long mInv5 = -3689348814741910323L;
        final long nDiv5 = 3689348814741910323L;
        int count = 0;
        for (;;) {
            value *= mInv5;
            if (Long.compareUnsigned(value, nDiv5) > 0) {
                break;
            }
            ++count;
        }
        return count;
    }

    // Check if value is multiple of 5^p
    // Optimized: uses modular inverse instead of division
    static boolean multipleOfPowerOf5(long value, int p) {
        return pow5Factor(value) >= p;
    }

    // Fallback for systems without 128-bit integers
    static long umul128(long a, long b, long[] productHi) {
        long aLo = Integer.toUnsignedLong((int)a);
        long aHi = Integer.toUnsignedLong((int)(a >> 32));
        long bLo = Integer.toUnsignedLong((int)b);
        long bHi = Integer.toUnsignedLong((int)(b >> 32));

        long b00 = aLo * bLo;
        long b01 = aLo * bHi;
        long b10 = aHi * bLo;
        long b11 = aHi * bHi;

        long b00Lo = Integer.toUnsignedLong((int)b00);
        long b00Hi = Integer.toUnsignedLong((int)(b00 >> 32));

        long mid1 = b10 + b00Hi;
        long mid1Lo = Integer.toUnsignedLong((int)(mid1));
        long mid1Hi = Integer.toUnsignedLong((int)(mid1 >> 32));

        long mid2 = b01 + mid1Lo;
        long mid2Lo = Integer.toUnsignedLong((int)(mid2));
        long mid2Hi = Integer.toUnsignedLong((int)(mid2 >> 32));

        long pHi = b11 + mid1Hi + mid2Hi;
        long pLo = (mid2Lo << 32) | b00Lo;

        productHi[0] = pHi;
        return pLo;
    }

    static long shiftright128(long lo, long hi, int dist) {
        return (hi << (64 - dist)) | (lo >>> dist);
    }

    static long mulShift64(long m, long[] mul, int j) {
        long[] high1Buf = new long[1];
        long low1 = umul128(m, mul[1], high1Buf);
        long high1 = high1Buf[0];
        long[] high0Buf = high1Buf; // reuse alloc since we extracted high1 into its own long
        umul128(m, mul[0], high0Buf);
        long high0 = high0Buf[0];
        long sum = high0 + low1;
        if (Long.compareUnsigned(sum, high0) < 0) {
            ++high1;
        }
        return shiftright128(sum, high1, j - 64);
    }

    // Precomputed powers of ten from 10^0 to 10^22. These
    // can be represented exactly using the double type.
    private static final double[] POWER_OF_TEN = {
            1e0, 1e1, 1e2, 1e3, 1e4, 1e5, 1e6, 1e7, 1e8, 1e9, 1e10, 1e11,
            1e12, 1e13, 1e14, 1e15, 1e16, 1e17, 1e18, 1e19, 1e20, 1e21, 1e22};

    // Main conversion function: decimal mantissa+exponent to IEEE 754 double
    // Optimized for JSON parsing with fast paths for edge cases
    public static double ryuS2dFromParts(long m10, int m10digits, int e10, boolean signedM) {
        // From Daniel Lemire's fast_double_parser which is an extraction of part of 'How to Read
        // Floating Point Numbers Accurately' by Willian D. Clinger: https://www.cs.uoregon.edu/Reports/TR-1990-001.pdf
        if (-22 <= e10 && e10 <= 22 && Long.compareUnsigned(m10, 9007199254740991L) <= 0) {
            double value = (double) m10;  // Lossless conversion for 0 <= i <= 2^53 - 1.

            if (e10 < 0) {
                value /= POWER_OF_TEN[-e10];
            } else {
                value *= POWER_OF_TEN[e10];
            }

            return signedM ? -value : value;
        }

        // Fast path: handle zero explicitly (e.g., "0.0", "0e0") or overflow/underflow early
        if (m10 == 0 || m10digits + e10 <= -324) return signedM ? -0.0 : 0.0;

        // Overflow to infinity
        if (m10digits + e10 >= 310) return Double.POSITIVE_INFINITY;

        // Convert decimal to binary: m10 * 10^e10 = m2 * 2^e2
        int e2;
        long m2;
        boolean trailingZeros;

        if (e10 >= 0) {
            // Positive exponent: multiply by 5^e10 and adjust binary exponent
            e2 = floorLog2(m10) + e10 + log2pow5(e10) - (DOUBLE_MANTISSA_BITS + 1);
            int j = e2 - e10 - ceilLog2pow5(e10) + DOUBLE_POW5_BITCOUNT;
            m2 = mulShift64(m10, DOUBLE_POW5_SPLIT[e10], j);
            trailingZeros = e2 < e10 || (e2 - e10 < 64 && multipleOfPowerOf2(m10));
        } else {
            // Negative exponent: divide by 5^(-e10)
            e2 = floorLog2(m10) + e10 - ceilLog2pow5(-e10) - (DOUBLE_MANTISSA_BITS + 1);
            int j = e2 - e10 + ceilLog2pow5(-e10) - 1 + DOUBLE_POW5_INV_BITCOUNT;
            m2 = mulShift64(m10, DOUBLE_POW5_INV_SPLIT[-e10], j);
            trailingZeros = multipleOfPowerOf5(m10, -e10);
        }

        // Compute IEEE 754 exponent
        int ieee_e2 = Math.max(0, e2 + DOUBLE_EXPONENT_BIAS + floorLog2(m2));
        int signedML = signedM ? 1 : 0;

        if (ieee_e2 > 0x7fe) {
            // Overflow to infinity
            return int64Bits2Double((signedML << 63) | 0x7ff0000000000000L);
        }

        // Compute shift amount for rounding
        int shift = (ieee_e2 == 0 ? 1 : ieee_e2) - e2 - DOUBLE_EXPONENT_BIAS - DOUBLE_MANTISSA_BITS;

        // IEEE 754 round-to-even (banker's rounding)
        trailingZeros &= (m2 & ((1L << (shift - 1)) - 1)) == 0;
        long lastRemovedBit = (m2 >> (shift - 1)) & 1;
        int roundUp = (lastRemovedBit != 0) && (!trailingZeros || (((m2 >> shift) & 1) != 0)) ? 1 : 0;

        long ieee_m2 = (m2 >> shift) + roundUp;
        ieee_m2 &= (1L << DOUBLE_MANTISSA_BITS) - 1;

        if (ieee_m2 == 0 && roundUp == 1) {
            ieee_e2++;
        }

        // Pack sign, exponent, and mantissa into IEEE 754 format
        // Match original Ryu: group sign+exponent, then shift and add mantissa
        long ieee = (((signedML << DOUBLE_EXPONENT_BITS) | (long)ieee_e2) << DOUBLE_MANTISSA_BITS) | ieee_m2;
        return int64Bits2Double(ieee);
    }
